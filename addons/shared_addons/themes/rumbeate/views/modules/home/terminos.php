						<span class="titulo_terminos">TÉRMINOS Y CONDICIONES RUMBÉATE CANCÚN</span> <br /><br />

Durex es una marca registrada y de propiedad de Reckitt Benckiser Colombia S.A.<br />
Esta actividad aplica para el territorio nacional. Pueden participar personas en todo el mundo, sin embargo la premiación solo se hará entre los residentes en Colombia, mayores de 18 años, cumplidos a la fecha de inicio de la activación. Si personas menores de edad se registran y participan, quedarán eliminados de forma inmediata en el momento de verificación de la edad y no tendrán derecho a la entrega de premios. <br /><br />

Este reglamento delimita y aclara las condiciones bajo las cuales se regirá la actividad:<br />
Lo aquí dispuesto será de acatamiento obligatorio para participantes y organizador. Se entenderá que todo participante conoce, entiende y acepta las condiciones y limitaciones establecidas en el presente reglamento. La participación en la actividad implica la decisión del participante de obligarse por estas reglas y por las decisiones del organizador, las cuales tendrán carácter definitivo en todos los asuntos relacionados a la actividad. Cualquier violación a las mismas o a los procedimientos o sistemas establecidos para su realización, implicará la inmediata exclusión de la participante y/o la eliminación del ganador.<br /><br />


De los Realizadores:<br />
RB Colombia S.A. es el organizador de la actividad. Como tal, será el único responsable de la misma.<br /><br />

De los participantes:<br />
Todas las personas interesadas en participar deberán ser mayores de 18 años y poseer un documento de identificación vigente (Cédula de ciudadanía, cedula de extranjería, pasaporte, etc.), el cual deberá presentarse si es acreedor de algún premio. Toda persona que desee participar en la actividad o reclamar el premio, deberá leer completamente el presente reglamento.  <br /><br />

Una vez leído, deberá aceptar los términos y condiciones previstos. Con la lectura y aceptación de los términos y condiciones los participantes admiten conocer y aceptar las condiciones de participación así como las condiciones, limitaciones y responsabilidades, no sólo de este reglamento, sino las que en virtud de este mismo documento conllevan el reclamo y aceptación de un premio.<br /><br />

Así mismo, se deja constancia que los premios de esta actividad se entregarán únicamente a personas que cumplan con los requisitos establecidos en este reglamento.<br /><br />


Vigencia:<br />
La actividad Inicia el 15 de Octubre de 2014 a las 8:00 horas y termina el 30 de Noviembre de 2014 a las 18:00 horas de Colombia.<br /><br />

Registro:<br />
Todos los Participantes deben comprar productos Durex de cualquier referencia y registrarse en la página de la actividad: https://www.rumbeatecancun.com, desde un PC o dispositivo móvil. La aplicación será creada por BRM S.A. y serán ellos los encargados del manejo de la aplicación en el sitio.<br /><br />


Mecánica:<br />
La actividad se desarrolla en el sitio: https://www.rumbeatecancun.com<br />
Para participar se deben seguir los siguientes pasos:<br /><br />

1. Comprar productos Durex de cualquier referencia. Aplica para condones, gel y/o anillo vibrador.<br />
2. Ingresar a la página web de la actividad https://www.rumbeatecancun.com<br />
3. Registrar sus datos personales y subir una fotografía de la factura de compra si esta fue hecha en establecimientos o registrar el código de la factura si la compra se realizó a través de www.tiendadurex.com . Es importante aclarar que la factura debe dejar clara la compra de productos durex y la fecha de compra, la cual debe estar entre el 15 de octubre y 30 de noviembre de 2014.<br />
4. Leer y aceptar los términos y condiciones. Si no se aceptan, no se podrá completar el registro.<br /><br />


Premios y Ganadores<br />
1. Viajes a Cancún: El 10 de Diciembre del 2014 se sortearán 5 viajes a Cancún, México. Este sorteo será aleatorio entre las personas que hayan participado en la actividad. Cada viaje está compuesto de:<br /><br />
- Tiquetes ida y vuelta (saliendo únicamente desde Bogota a la ciudad de Cancún)* para 3 personas.<br />
- Estadía 5 días 4 noches en el hotel Crown Paradise® Cancún Habitación sencilla. Incluye estadía para 3 personas.**<br />
- Incluye desayuno, almuerzo y comida – impuestos hoteleros para toda la estadía<br />
- Traslado aeropuerto – hotel – aeropuerto<br />
- US$1.500 (dólares americanos)  los cuales serán entregados en tarjeta debito. <br /><br />

*Si el ganador no reside en Bogotá, el deberá asumir los costos de transporte desde su ciudad de residencia en Colombia a la ciudad de Bogotá. <br /><br />

** Acomodación sujeta a disponibilidad <br />
Los tiquetes solo podrán ser utilizados entre el 13 de enero y el 21 de marzo de 2015. Para esta fecha el ganador y sus dos acompañantes deberán tener pasaporte vigente. <br /><br />

Es importante aclarar que para el viaje a Cancún, México no se necesita visa.<br /><br />


Publicación de ganadores: <br />
Los ganadores se publicarán 15 días calendario después de terminada la actividad, en las redes sociales de Durex Colombia. <br /><br />


Condiciones y Restricciones<br /><br />

Condiciones generales<br />
- La fotografía de la factura o recibo deberá ser clara, legible y deberá evidenciar la fecha, el valor y los productos Durex adquiridos. Si la fotografía de la factura no es legible, no entrará en el concurso. <br />
- La factura o recibo deberá tener fecha entre el 15 de octubre del 2014 y el 30 de noviembre de 2014.<br />
- No serán válidas las facturas de compras realizadas en Mercadolibre.com. 
- Cada factura tiene solo una (1) oportunidad de participar. Subir la misma factura varias veces podrá ser causal de descalificación para participar.<br />
- El nombre y los datos registrados en la aplicación, deberán coincidir con los datos que figuran en su documento de identidad.<br />
- Si el usuario realizó la compra de productos a través de www.tiendadurex.com, deberá digitar el número de la factura en el registro.<br />
- Para viajar a Cancún con Durex, el usuario deberá poseer pasaporte vigente. Es responsabilidad del ganador y sus acompañantes tener todos sus documentos de viaje al día, al igual que los documentos que en el momento de redención del tiquete exijan las autoridades mexicanas. <br />
- No podrán participar personas que trabajen con RB de manera directa o personas y proveedores que hayan estado relacionadas con el desarrollo de esta actividad.<br />
- Los premios son intransferibles. La única persona que puede utilizar el tiquete será la ganadora y es ella la responsable de definir los nombres de sus dos acompañantes. ( El premio no se puede ceder por ningún motivo)- <br /><br />


Con relación a la marca<br />
Siempre que se menciona, Marca # 1 de condones en el mundo se hace referencia a la Marca #1 en ventas, en el mundo, según Nielsen al cierre de 2.013<br /><br />


Entrega de Premios:<br />
Durante los 10 días hábiles siguientes a la definición del ganador, este será contactado vía telefónica o por e-mail para informar la mecánica de entrega del premio. La persona ganadora deberá responder dentro de los 2 días hábiles siguientes a ser contactados con el fin de aceptar la entrega del premio y confirmar la información suministrada. <br /><br />

El ganador (después de que sea contactado) deberá enviar su cédula o documento de identidad escaneado, vía mail, a los organizadores del sorteo para certificar su identidad y validar los datos que fueron registrados.<br /><br />

Adicionalmente, únicamente si el ganador no es localizado vía telefónica o por e-mail dentro de los días mencionados anteriormente, no cumple con los requisitos indicados en este reglamento o sus datos son falsos, corresponden a otra persona o son incongruentes con los que se aportaron para participar, el premio será considerados vacantes y se cederá al siguiente ganador en la lista, en cuyo caso se informará del cambio de ganador por las siguientes vías: correo electrónico o por publicación en las redes sociales de Durex Colombia.<br /><br />

Los organizadores no tendrán la obligación de investigar los motivos por los cuales una persona no es contactada en el número de teléfono registrado o sus datos no concuerdan con el registro respectivo o base de datos ni tampoco el motivo de no aceptación de los premios.<br /><br />


Plazos para entrega:<br />
Las fechas para utilizar el tiquete y la estadía deberán ser entre el 13 de enero y el 21 de marzo de 2015.<br />
Al momento de recibir el premio, el ganador del mismo deberá entregar una fotocopia de su documento de identidad y firmar el acta de recibido. <br /><br />

Consideraciones importantes:<br />
Cada concursante autoriza a RB para dar a conocer los resultados de la actividad, publicar su nombre entre los ganadores (en caso de que aplique) o dar a conocer información alusiva a la misma, realizar material audiovisual sobre el concurso y los premios, en cualquier medio publicitario. Igualmente, autoriza que se use fotografías del perfil  con el cual participó y cualquier otra fotografía que sea tomada durante la entrega del premio.<br /><br />

Si el ganador no acepta el beneficio o condiciones del premio o las condiciones contenidas en este reglamento, se considera que renuncia al mismo. Además se entiende que renuncia a cualquier reclamo que pudiera ejercer en cualquier instancia incluyendo las instancias administrativas y/o judiciales, y también renuncia a  ejercer acciones de indemnización total o parcial contra Reckitt Benckiser y sus marcas.<br /><br />

